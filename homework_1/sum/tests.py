import pytest
from main import max_even_sum


@pytest.mark.parametrize(
    "nums, expected",
    [
        ([], 0),  # пустой массив
        ([2], 2),  # один чётный
        ([1], 0),  # один нечётный -> пустое подмножество
        ([1, 1], 2),  # оба нечётные -> сумма чётная
        ([1, 2], 2),  # 3 -> 3 - min_odd(1) = 2
        ([2, 4, 6], 12),  # всё чётное
        ([1, 3, 5], 8),  # 9 - 1 = 8
        ([1, 1, 1], 2),  # 3 - 1 = 2
        ([7, 7], 14),  # уже чётная сумма
        ([7, 2, 4], 6),  # 13 - 7 = 6
        ([5, 5, 2], 12),  # сумма чётная
        ([1000000000000000001, 2], 2),  # большие числа, чётность ок
    ],
)
def test_various(nums, expected):
    assert max_even_sum(nums) == expected
